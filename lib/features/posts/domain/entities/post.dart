class Post {
  int id;
  int user;
  String text;
  String published;
  String media;
  String image;
  String location;
  int num_likes;
  String first_name;
  String last_name;
  String url_image;

  Post({
    required this.id,
    required this.user,
    required this.text,
    required this.published,
    required this.media,
    required this.image,
    required this.location,
    required this.num_likes,
    required this.first_name,
    required this.last_name,
    required this.url_image,
  });
}
